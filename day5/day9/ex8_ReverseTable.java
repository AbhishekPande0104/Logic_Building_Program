package day9;

/*
 * Write a program to print reverse table of 12.
 */

public class ex8_ReverseTable 
{
	public static void main(String[] args)
	{
		System.out.println("The reverse table of 12 is ");
		int n=12;
		for(int i=10; i>=1; i--)
		{
			int mul=n*i;
			System.out.println(mul);
		}
	}
}

