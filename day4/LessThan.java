package day4;

public class LessThan 
{
	public static void main(String[] args)
	{
		int num=19;
		if(num==10)
		{
			System.out.println(num+" is equal to 10");
		}
		else if(num<10)
		{
			System.out.println(num+" is less than 10");
		}
		else
		{
			System.out.println(num+" is more than 10");
		}
	}
}
